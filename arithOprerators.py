#Python Arithmetic Operations
num1 = float(input("Enter Number Uno: "))
num2 = float(input("Enter Number Dos: "))

#Addition
add = num1 + num2
#Substraction
sub = num1 - num2
#Multiplication
multi = num1 * num2
#Division
div = num1 / num2

print("Addition: ", add)
print("Substraction: ", sub)
print("Multiplication: ", multi)
print("Division: ", div)